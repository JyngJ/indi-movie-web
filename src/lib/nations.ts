/** "대한민국" → "🇰🇷 대한민국", 매핑 없으면 그대로 반환 */
export function withFlag(nation: string): string {
  const flag = FLAG_MAP[nation.trim()]
  return flag ? `${flag} ${nation}` : nation
}

/** "대한민국, 미국" 같은 쉼표 구분 문자열 → "🇰🇷 대한민국, 🇺🇸 미국" */
export function withFlagsRaw(raw: string): string {
  return raw
    .split(/[,，/·]+/)
    .map(s => withFlag(s.trim()))
    .join(', ')
}
